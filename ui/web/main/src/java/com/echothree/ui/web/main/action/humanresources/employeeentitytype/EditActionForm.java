package com.echothree.ui.web.main.action.humanresources.employeeentitytype;

import com.echothree.model.control.core.common.choice.ApplicationEditorChoicesBean;
import com.echothree.view.client.web.struts.BaseActionForm;
import com.echothree.view.client.web.struts.sprout.annotation.SproutForm;
import javax.servlet.http.HttpServletRequest;
import org.apache.struts.action.ActionMapping;

@SproutForm(name="EmployeeEntityTypeEdit")
public class EditActionForm
        extends BaseActionForm {

    private String partyName;
    private String componentVendorName;
    private String entityTypeName;
    private Boolean confirmDelete;

    public String getPartyName() {
        return partyName;
    }

    public void setPartyName(String partyName) {
        this.partyName = partyName;
    }

    public void setComponentVendorName(String componentVendorName) {
        this.componentVendorName = componentVendorName;
    }
    
    public String getComponentVendorName() {
        return componentVendorName;
    }
    
    public void setEntityTypeName(String entityTypeName) {
        this.entityTypeName = entityTypeName;
    }
    
    public String getEntityTypeName() {
        return entityTypeName;
    }

    public void setConfirmDelete(Boolean confirmDelete) {
        this.confirmDelete = confirmDelete;
    }
    
    public Boolean getConfirmDelete() {
        return confirmDelete;
    }
    
    @Override
    public void reset(ActionMapping mapping, HttpServletRequest request) {
        super.reset(mapping, request);
        
        confirmDelete = false;
    }
    
}
